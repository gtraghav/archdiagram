// IPC: Load custom icons — scans directory for SVGs, auto-generates manifest (no manifest.json needed)
ipcMain.handle('icons:load-custom', async () => {
  const settings = await readSettings()
  const customIconsDir = settings.customIconsDir ?? DEFAULT_ICONS_DIR

  if (!existsSync(customIconsDir)) return null

  try {
    const allFiles = readdirSync(customIconsDir)
    const svgFiles = allFiles.filter((f) => f.toLowerCase().endsWith('.svg'))
    if (svgFiles.length === 0) return null

    // Group icons by service (derived from filename: Res_<Service>_<Resource>_48.svg)
    const categoryMap = new Map<string, Array<{ id: string; name: string; fileName: string }>>()

    for (const fileName of svgFiles) {
      const base = fileName.replace(/\.svg$/i, '')
      // Strip leading "Res_" and trailing "_48" (or similar size suffix)
      const stripped = base.replace(/^Res_/, '').replace(/_\d+$/, '')
      const underscoreIdx = stripped.indexOf('_')
      let service: string
      let resource: string
      if (underscoreIdx === -1) {
        service = 'Custom'
        resource = stripped
      } else {
        service = stripped.slice(0, underscoreIdx).replace(/-/g, ' ')
        resource = stripped.slice(underscoreIdx + 1).replace(/-/g, ' ')
      }

      const id = `custom_${base.toLowerCase().replace(/[^a-z0-9]+/g, '_')}`
      if (!categoryMap.has(service)) categoryMap.set(service, [])
      categoryMap.get(service)!.push({ id, name: resource, fileName })
    }

    // Build manifest
    const categories = Array.from(categoryMap.entries()).map(([name, icons]) => ({
      name,
      icons: icons.map(({ id, name: iconName, fileName }) => ({
        id,
        name: iconName,
        provider: 'custom' as const,
        category: name,
        tags: iconName.toLowerCase().split(' '),
        svgPath: fileName,
      })),
    }))
    const manifest = { provider: 'custom' as const, version: '1.0', categories }

    // Load SVG contents
    const svgs: Record<string, string> = {}
    for (const cat of categories) {
      for (const icon of cat.icons) {
        const svgPath = join(customIconsDir, icon.svgPath)
        svgs[icon.id] = await readFileAsync(svgPath, 'utf-8')
      }
    }

    return { manifest, svgs }
  } catch (err) {
    console.error('icons:load-custom error', err)
    return null
  }
})

// IPC: Get current settings
ipcMain.handle('settings:get', async () => {
  return readSettings()
})

// IPC: Update settings
ipcMain.handle('settings:set', async (_event, partial: Record<string, unknown>) => {
  return writeSettings(partial)
})

// IPC: Open native folder picker for icon directory
ipcMain.handle('settings:pick-icons-dir', async () => {
  const win = BrowserWindow.getFocusedWindow()
  if (!win) return null

  const { canceled, filePaths } = await dialog.showOpenDialog(win, {
    title: 'Select Custom Icons Directory',
    properties: ['openDirectory'],
  })
  return canceled || filePaths.length === 0 ? null : filePaths[0]
})
