import { app, BrowserWindow, ipcMain, dialog, protocol, net, nativeImage } from 'electron'
import { join } from 'path'
import { readFile, writeFile, mkdir } from 'fs/promises'
import { existsSync } from 'fs'
import { pathToFileURL } from 'url'
import { buildMenu } from './menu'
import { readFile as fsReadFile, readdirSync } from 'fs'
import { promisify } from 'util'
import { readSettings, writeSettings, DEFAULT_ICONS_DIR } from './settingsStore'

const readFileAsync = promisify(fsReadFile)

app.setName('ArchDiagram')

// Set dock icon (works in dev; packaged builds use electron-builder icon config)
const iconPath = join(app.getAppPath(), 'resources/icon.icns')
if (existsSync(iconPath) && app.dock) {
  app.dock.setIcon(nativeImage.createFromPath(iconPath))
}

const isDev = !app.isPackaged

function createWindow(): BrowserWindow {
  const win = new BrowserWindow({
    width: 1440,
    height: 900,
    titleBarStyle: 'hiddenInset',
    webPreferences: {
      preload: join(__dirname, '../preload/preload.js'),
      contextIsolation: true,
      sandbox: false,
    },
  })

  if (isDev) {
    win.loadURL('http://localhost:5173')
    win.webContents.openDevTools()
  } else {
    win.loadFile(join(__dirname, '../renderer/index.html'))
  }

  return win
}

// Register archdoc:// protocol for secure local asset loading
app.whenReady().then(() => {
  protocol.handle('archdoc', (request) => {
    const url = request.url.replace('archdoc://', '')
    const filePath = join(app.getAppPath(), url)
    return net.fetch(pathToFileURL(filePath).toString())
  })

  const win = createWindow()
  buildMenu(win)

  // Ensure default custom icons directory exists
  const defaultCustomIconsDir = join(app.getPath('userData'), 'custom-icons')
  if (!existsSync(defaultCustomIconsDir)) {
    mkdir(defaultCustomIconsDir, { recursive: true }).catch(console.error)
  }

  app.on('activate', () => {
    if (BrowserWindow.getAllWindows().length === 0) {
      const newWin = createWindow()
      buildMenu(newWin)
    }
  })
})

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') app.quit()
})

// IPC: Open file
ipcMain.handle('file:open', async () => {
  const win = BrowserWindow.getFocusedWindow()
  if (!win) return null

  const { canceled, filePaths } = await dialog.showOpenDialog(win, {
    filters: [{ name: 'Architecture Diagrams', extensions: ['archdoc.json', 'json'] }],
    properties: ['openFile'],
  })

  if (canceled || filePaths.length === 0) return null

  const content = await readFile(filePaths[0], 'utf-8')
  return { filePath: filePaths[0], content: JSON.parse(content) }
})

// IPC: Save file
ipcMain.handle('file:save', async (_event, { content, filePath }: { content: unknown; filePath?: string }) => {
  const win = BrowserWindow.getFocusedWindow()
  if (!win) return null

  let savePath = filePath
  if (!savePath) {
    const { canceled, filePath: chosen } = await dialog.showSaveDialog(win, {
      defaultPath: 'diagram.archdoc.json',
      filters: [{ name: 'Architecture Diagrams', extensions: ['archdoc.json', 'json'] }],
    })
    if (canceled || !chosen) return null
    savePath = chosen
  }

  await writeFile(savePath, JSON.stringify(content, null, 2), 'utf-8')
  return savePath
})

// IPC: Export PNG (data URL from renderer → write file)
ipcMain.handle('export:png', async (_event, { dataUrl, outputPath }: { dataUrl: string; outputPath: string }) => {
  const base64 = dataUrl.replace(/^data:image\/png;base64,/, '')
  await writeFile(outputPath, Buffer.from(base64, 'base64'))
})

// IPC: Write raw base64 bytes (used for PDF generated by jsPDF in renderer)
ipcMain.handle('export:raw', async (_event, { base64, outputPath }: { base64: string; outputPath: string }) => {
  await writeFile(outputPath, Buffer.from(base64, 'base64'))
})

